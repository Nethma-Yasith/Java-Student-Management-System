    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please Select Row", "Warning", JOptionPane.INFORMATION_MESSAGE);
        } else {

            String id = String.valueOf(jTable1.getValueAt(selectedRow, 0));

            String firstName = jTextField1.getText();
            String lastName = jTextField2.getText();
            String mobile = jTextField3.getText();
            String email = jTextField4.getText();
            String address = jTextField7.getText();

            String birthday = jTextField5.getText();
            String gName = jTextField8.getText();
            String gmobile = jTextField9.getText();

            ButtonModel genderSelection = buttonGroup1.getSelection();

            if (firstName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Type First Name", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else if (lastName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Type Last Name", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else if (mobile.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Type mobile Number", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else if (email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Type Email", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else if (address.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Type Adress Line1", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else if (birthday.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Type Birthday", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else if (gName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Type Gardiyan Name", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else if (gmobile.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Type Gardiyan Mobile", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else if (genderSelection == null) {
                JOptionPane.showMessageDialog(this, "Please Select Gender", "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else {
                String genderId = genderSelection.getActionCommand();

                try {
                    MySQL.execute("UPDATE `student` SET "
                            + "`fname`='" + firstName + "',"
                            + "`lname`='" + lastName + "',"
                            + "`mobile`='" + mobile + "',"
                            + "`email`='" + email + "',"
                            + "`date_of_birth`='" + birthday + "',"
                            + "`g_name`='" + gName + "',"
                            + "`g_mobile`='" + gmobile + "',"
                            + "`address`='" + address + "',"
                            + "`gender_id`='" + genderId + "'"
                            + "WHERE `sno`='" + id + "'");

                    loadStudents();
                    reset();

                    jTable1.setEnabled(true);
                    jButton1.setEnabled(true);
                    jTextField5.setEnabled(true);
                    jTextField6.setEnabled(true);
                    jRadioButton1.setEnabled(true);
                    jRadioButton2.setEnabled(true);

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

