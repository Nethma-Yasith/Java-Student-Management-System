    private void loadStudents() {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `student`"
                    + "INNER JOIN `gender` ON `student`.`gender_id`=`gender`.`id` ORDER BY `sno` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> v = new Vector();

                v.add(resultSet.getString("sno"));
                v.add(resultSet.getString("fname"));
                v.add(resultSet.getString("lname"));
                v.add(resultSet.getString("mobile"));
                v.add(resultSet.getString("email"));
                v.add(resultSet.getString("address"));
                v.add(resultSet.getString("date_of_birth"));
                v.add(resultSet.getString("g_name"));
                v.add(resultSet.getString("g_mobile"));
                //v.add(resultSet.getString("date"));

                v.add(resultSet.getString("gender.type"));

                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

